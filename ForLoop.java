public class Main {
  public static void main(String[] args) {

    for (int i = 0; i <= 5; i++) {
      System.out.println(i);
    }

    String[] cars = { "Honda", "BMW", "Ford", "lamborghini" };
    for (String i : cars) {
      System.out.println(i);
    }

    System.out.println();

    String[] students = { "ramesh", "hamesh", "kamlesh", "suresh", "hamesh" };
    for (String i : students) {
      System.out.println(i);
    }

    for (int i = 0; i < 10; i++) {
      System.out.println(i);
    }

  }
}